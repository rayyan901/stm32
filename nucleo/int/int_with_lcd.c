char* intToStr(uint16_t number, char* buffer) {
    // Check if the buffer is valid
    if (buffer == NULL) {
        return NULL;
    }

    // Check if the number is within the valid 3-digit range (0-999)
    if (number > 999) {
        // If the number is out of range, return an error string
        buffer[0] = 'E';
        buffer[1] = 'R';
        buffer[2] = 'R';
        buffer[3] = '\0'; // Null-terminate the error string
        return NULL; // Indicate an error
    }

    // Extract hundreds digit
    uint8_t hundreds = number / 100;
    // Extract tens digit (remaining number after hundreds, then divide by 10)
    uint8_t tens = (number % 100) / 10;
    // Extract units digit (remaining number after tens)
    uint8_t units = number % 10;

    // Convert digits to ASCII characters and store them in the buffer
    // Add '0' (ASCII 48) to the digit value to get its ASCII character representation
    buffer[0] = hundreds + '0'; // Hundreds place
    buffer[1] = tens + '0';     // Tens place
    buffer[2] = units + '0';    // Units place
    buffer[3] = '\0';           // Null-terminate the string

    return buffer; // Return a pointer to the converted string
}

